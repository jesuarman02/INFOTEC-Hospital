import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IPacienteMedicamento } from '@/shared/model/pacientems/paciente-medicamento.model';

const baseApiUrl = 'services/pacientesms/api/paciente-medicamentos';

export default class PacienteMedicamentoService {
  public find(id: number): Promise<IPacienteMedicamento> {
    return new Promise<IPacienteMedicamento>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IPacienteMedicamento): Promise<IPacienteMedicamento> {
    return new Promise<IPacienteMedicamento>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IPacienteMedicamento): Promise<IPacienteMedicamento> {
    return new Promise<IPacienteMedicamento>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IPacienteMedicamento): Promise<IPacienteMedicamento> {
    return new Promise<IPacienteMedicamento>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
